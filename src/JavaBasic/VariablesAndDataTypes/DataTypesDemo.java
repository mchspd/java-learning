package JavaBasic.VariablesAndDataTypes;

public class DataTypesDemo {
    public static void main(String[] args) {
        byte mByte = 100;
        short mShort = 10000;
        int mInt = 1000000000;
        long mLong = 1000000000000L;

        float mFloat = 5.5f;
        double mDouble = 20.01;

        char mChar = 'J';
        boolean mBool = true;

        System.out.println("byte: " + mByte);
        System.out.println("short: " + mShort);
        System.out.println("int: " + mInt);
        System.out.println("long: " + mLong);
        System.out.println("float: " + mFloat);
        System.out.println("double: " + mDouble);
        System.out.println("char: " + mChar);
        System.out.println("boolean: " + mBool);
        System.out.println("------------------------------");
        System.out.println("casting");

        int a = 10;
        long b = a;
        System.out.println(b);
        double x = 5.57;
        int y = (int)x;
        System.out.println(x);
        System.out.println(y);













































    }
}
