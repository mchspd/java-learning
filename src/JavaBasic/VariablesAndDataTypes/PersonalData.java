package JavaBasic.VariablesAndDataTypes;

public class PersonalData {
    public static void main(String[] args) {
        byte age = 33;
        short birthYear = 1992;
        int population = 11000;
        long distanceToSun = 1391400L;
        float weight = 80.2f;
        double height = 1.84;
        char gender = 'M';
        boolean isStudent = false;

        System.out.println("Age: " + age);
        System.out.println("Year of birth: " + birthYear);
        System.out.println("City population: " + population);
        System.out.println("Distance from home planet to Sun: " + distanceToSun + " km");
        System.out.println("Weight: " + weight + " kg");
        System.out.println("Height: " + height + " m");
        System.out.println("Gender: " + gender);
        System.out.println("Student? " + isStudent);

    }
}
